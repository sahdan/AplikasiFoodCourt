    private void tblAntrianMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAntrianMousePressed
        // TODO add your handling code here:
        int baris, jumlah, meja;
        String nama, IdTransaksi, IdMenu;
        baris = tblAntrian.getSelectedRow();
        if (baris != -1) {
            nama = tblAntrian.getModel().getValueAt(baris,1).toString();
            jumlah = (int)tblAntrian.getModel().getValueAt(baris,2);
            meja = (int)tblAntrian.getModel().getValueAt(baris,3);

            int pilihan = JOptionPane.showConfirmDialog(this, "Konfirmasi "+ jumlah
                    + " " + nama + " untuk Meja " + meja+ " sudah siap",
                        "Konfirmasi", JOptionPane.YES_NO_OPTION);
                if (pilihan == JOptionPane.YES_OPTION) {
                    IdTransaksi = arrAntrian.get(baris)[0];
                    IdMenu = arrAntrian.get(baris)[1];
                    String sql = "UPDATE tblrinciantransaksi SET Status = 'siap' "
                            + "WHERE IdTransaksi = ? AND IdMenu = ?";
                    try {
                        ps = conn.prepareStatement(sql);
                        ps.setString(1, IdTransaksi);
                        ps.setString(2, IdMenu);
                        ps.executeUpdate();
                        refreshTable();
                    } catch (SQLException ex) {
                        Logger.getLogger(FrmSiap.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
        }
    }//GEN-LAST:event_tblAntrianMousePressed

