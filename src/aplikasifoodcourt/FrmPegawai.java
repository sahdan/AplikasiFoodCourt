    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        try
        {
            st = conn.createStatement();
            String search = tflCari.getText();
            rs = st.executeQuery("SELECT a.IdPegawai, a.Nama, b.Username, a.Gender, a.Jabatan, a.Aktif from tblpegawai a, tblUser b "
                    + "WHERE a.IdPegawai = b.Id AND (a.IdPegawai like '%" + search + "%' OR a.Nama like '%"
                    +search+ "%' OR a.Gender like '%" + search + "%' OR a.Jabatan like '%" + search + "%' OR b.Username like '%"
                    +search+ "%')");
            model = (DefaultTableModel) tblPegawai.getModel();

            model.setRowCount(0);
            String [] data = new String[6];

            while(rs.next())
            {
                data[0] = rs.getString("IdPegawai");
                data[1] = rs.getString("Nama");
                data[2] = rs.getString("Username");
                data[3] = rs.getString("Gender");
                data[4] = rs.getString("Jabatan");
                data[5] = rs.getString("Aktif");
                model.addRow(data);
            }
            rs.close();
        }
        catch(SQLException ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }                          

    }//GEN-LAST:event_btnCariActionPerformed

