    private void refreshTable() {
        try { 
            model.setRowCount(0);
            String sql = "select a.IdPegawai, a.Nama, b.Username, a.Gender, a.Jabatan, a.Aktif from tblpegawai a, tblUser b "
                    + "WHERE a.IdPegawai = b.Id";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                });
            }
           tblPegawai.setModel(model);
           lebarKolom();
           rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(FrmPegawai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

